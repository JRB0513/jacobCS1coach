class divideSeven
{
    public static void main(String args[])
    {
        int even1 = 63;
        int odd1 = 23;
        int eventhing = (even1 % 7);
        int oddthing = (odd1 % 7);
        {
            System.out.println("If we divide " + even1 + " by 7 the remainder is " + eventhing + " so it must be evenly divisible by 7.");
            System.out.println("If we divide " + odd1 + " by 7 the remainder is " + oddthing + " so it must not be evenly divisible by 7.");
        }
    }
}